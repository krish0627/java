package tutorial;

public class datatype {
    public static void main(String[] args){
        int marks =85;
        boolean examPassed = true;
        char grade = 'A';
        double gpa = 3.25;
        String remark =" congrats!!!, you passed";

        System.out.println("Integer value (marks): " + marks);
        System.out.println("Double value (gpa): " + gpa);
        System.out.println("Boolean value (examPassed): " + examPassed);
        System.out.println("Character value (grade): " + grade);
        System.out.println("String value (remark): " + remark);
    }
}
